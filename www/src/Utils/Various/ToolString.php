    public function displaySmartTimeDiff($dateTimeStart, $dateTimeEnd = null)
    {
        if (!$dateTimeEnd) {
            $dateTimeEnd = new \DateTime(date('Y-m-d H:i:s'));
        }

        $diff = $dateTimeStart->diff($dateTimeEnd);

        if($diff->y) {
            $return = $diff->y.' annee';
            if ($diff->y > 1) {
                $return .= 's';
            }
            return $return;
        } elseif ($diff->m) {
            $return = $diff->m.' mois';
            return $return;
        } elseif ($diff->d) {
            $return = $diff->d.' jour';
            if ($diff->d > 1) {
                $return .= 's';
            }
            return $return;
        } elseif ($diff->h) {
            $return = $diff->h.' heure';
            if ($diff->h > 1) {
                $return .= 's';
            }
            return $return;
        } elseif ($diff->i) {
            $return = $diff->i.' minute';
            if ($diff->i > 1) {
                $return .= 's';
            }
            return $return;
        } else {
            return 'un instant';
        }
    }


    public function displaySmartDistance($distance)
    {
        // inférieur à 1 km
        if ($distance < 1) {
            return (round($distance * 10) * 100). ' m';
        } else {
            $return = round($distance, 1).' km';
            return str_replace('.', ',', $return);
        }
    }


