    /**
     * Fonction qui retourne un nom nettoyé (sans accent, espace, etc...)
     * @param $string
     * @param string $replacement
     * @return mixed
     */
    public function getSlug($string, $replacement = '-')
    {
        $string = strtolower(self::getNoAccent($string));

        // retire tout ce qui n'est ni chiffres ni lettres
        $pattern = '/[^a-z0-9]/';

        $string = preg_replace($pattern, $replacement, $string);

        while (substr($string, -1) == '-') {
            $string = substr($string, 0, strlen($string)-1);
        }
        $string = preg_replace($pattern, $replacement, $string);

        $pattern = '/-+/i';
        $replacement = '-';

        return preg_replace($pattern, $replacement, $string);
    }

    /**
     * @param int $length
     * @return string
     */
    public function generateSimpleCode($length = 10)
    {
        $chars = 'ABCDEFGHJKLMNPQRSTUVWXYZ23456789';
        $count = mb_strlen($chars);

        for ($i = 0, $result = ''; $i < $length; $i++) {
            $index = rand(0, $count - 1);
            $result .= mb_substr($chars, $index, 1);
        }

        return $result;
    }
